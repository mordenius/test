	<h2>Конгресс шеф-поваров FONTEGRO UKRAINE</h2>
	  <div>
		<p>Впервые с 4 по 6 ноября 2015 года, в рамках ресторанной выставки ЭКСПО 2015, Конгресс шеф-поваров FONTEGRO UKRAINE на совместном стенде с компанией "НОВЫЙ ПРОЕКТ" представили кулинарное шоу от лучших шеф-поваров Украины. Digital поддержкой в данном проекте выступила компания KVINTO c партнерами LiveBoard и Samsung, где продемонстрировала потенциальные возможности визуализации информации с помощью цифровых технологий, представила  </p>
	  </div>
	  <div>
		<p>прототип электронного меню и показала широкий функционал системы Digital Signage с  удаленной управляемостью контентом в режиме реального времени.  
Каждый день на стенде гости могли не только посещать мастер-классы 
шеф-поваров, дегустировать их кулинарные шедевры, а и удостовериться, на сколько технологии Digital Signage помогают человеку (потребителю, клиенту, спикеру и т.д.) в таком сегменте, как HoReCa, а именно:  </p>
	  </div>
