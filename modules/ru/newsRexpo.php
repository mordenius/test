	  <h2>Легкость восприятия</h2>
	  <div>
		<p>На протяжении 3-х дней конгресса в режиме нон-стоп любой гость и участник всегда был проинформирован и осведомлен о текущих и предстоящих событиях на стенде. Система показывала анонсы, тайминг выступлений, промо партнеров/участников, онлайн трансляцию работы шеф-поваров и демонстрировала их кулинарные шедевры. Что же в итоге мы получили?</p>
	  </div>
	  <div>
		<p>Поток осведомленной, а главное заинтересованной целевой аудитории в тех или иных сферах деятельности представленных на стенде. Что из этого может получить заведение общественного питание? Все тот же осведомленный поток целевой аудитории, знающий что и когда будет происходить у вас в заведении.</p>
