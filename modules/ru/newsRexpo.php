	  <h2>Своевременность подачи информации</h2>
	  <div>
		<p>Шеф-повар, создавая новое кулинарное творение, понимает, что жизненный цикл блюда заканчивается ровно на том моменте, как только столовый прибор его касается и в памяти остается исключительно вкус. В данном случае спикер имел возможность демонстрировать на экранах результат своей деятельности на протяжении всего дня, а также, что является немало важным для самого шефа – </p>
	  </div>
	  <div>
		<p>донести краткую информацию о себе и своих убеждениях, философию приготовления, тематику выступления и наименования фирменных блюд. Использование таких технологий в сегменте HoReCa создает безусловный wow-эффект. Также взаимодействует с аудиторией, за счет актуальной, своевременной и целевой подачи информации параллельно с онлайн трансляцией и описанием технологий.  </p>
