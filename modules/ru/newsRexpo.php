	  <h2>Взаимодействие и вовлеченность</h2>
	  <div>
		<p>Компания KVINTO имела возможность продемонстрировать прототип электронного меню с определенным функционалом, разработанным специально под формат мероприятия. Данное приложение включало в себя возможности:</p>
	  </div>
	  <div>
		<p>- заказа для дегустации любого блюда из списка повара;<br>
			- просмотра расписания и анонсов мастер-классов на ближайшие дни;<br>
		  - ознакомиться более детально с каждым спикером;<br>
			- дать оценку и проголосовать за понравившееся блюда;<br>
			- получения обратной связи от участников той или иной сферы деятельности, представленной на стенде (KVINTO, LiveBoard, Samsung, Новый проект, Форвард);<br>
			- сбора данных о пользователях для дальнейшей коммуникации.</p>
