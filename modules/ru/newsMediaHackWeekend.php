	<div class="text-wrapper">
	  <h2>Есть запрос – есть реклама</h2>
	  <div>
		<p>Целью является избирательный подход к выводу рекламной информации, на основе предпочтений каждого пользователя этой системы. Потребитель получает отсечение ненужной, навязчивой и надоедливой информации. Рекламодатель в свою очередь, гарантированное донесение материала исключительно целевой аудитории. </p>
	  </div>
	  <div>
		<p>Обычная тв реклама вещается по прайм тайму и не дает никакого фидбека о том, кто посмотрел эту рекламу и была ли она ему интересна.  Digital target может отследить целевой поток и перевести оплату за показ рекламы не в эфирное время, а за конкретные целевые просмотры. Ну и для владельцев рекламных площадок - повышение качества услуг.</p>
	  </div>
