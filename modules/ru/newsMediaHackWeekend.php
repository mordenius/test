	<h2>Future Media Lab</h2>
	  <div>
		<p>С 16 по 18 октября 2015 года в рамках Future Media Lab состоялся масштабный в Украине хакатон Media Hack Weekend на легендарной киностудии имени Довженко . 
		Группа компаний «1+1 Медиа» собрала самых креативных молодых людей для того, что бы познакомить их с профессионалами медиарынка, технологического бизнеса, 
		потенциальными инвесторами и дать уникальную возможность зажечь специалистов своими намерениями. </p>
	  </div>
	  <div>
		<p>Или же другими словами некая игра с лучшими, в итоге которой можно прийти с идеей, а уйти с первым корпоративным клиентом.
			На протяжении этих дней участники ломали головы над изобретениями, которые сделают Интернет, телевидение, компьютерные игры такими,что будут поражать еще больше. 
			И конечно же команда KVINTO с партнерами LiveBoard, в составе единой команды, не стали исключением и приняли участие в самом масштабном медиа-мероприятии Украины. </p>
