	  <h2>Решение Smart Target</h2>
	  <div>
		<p>Что же было разработано в итоге? Smart target – система, отвечающая за
 персонализацию рекламного контента и включающая в себя приложение Smart Discount. В данном случае был использован дискаунтовый контент ТРЦ. </p>
	  </div>
	  <div class="clearfix"></div>
	</div>
	<img src="img/news/MediaHackWeekend/KW.MHV.Image_6.jpg" title="" alt="" />
	<div class="clearfix"></div>
	<div class="text-wrapper">
	  <h2>Приложение Smart Discout</h2>
	  <div>
		<p>Что это такое и как с этим работать? Установив приложение Smart Discount, пользователь получает скидки или информационный рекламный контент соответствующий своим индивидуальным предпочтениям.  </p>
	  </div>
	  <div>
		<p>Проходя мимо видеоборда или digital киоска в ТРЦ, система Smart target
 показывает персонализированную рекламу в рамках интересов пользователя с помощью обмена информацией по Bluetooth между цифровой вывеской и смартфоном. </p>
	  </div>
	  <div class="clearfix"></div>
	</div>
	<img src="img/news/MediaHackWeekend/KW.MHV.Image_7.jpg" title="" alt="" />
