	  <h2>Проблема направления Digital Signage «Out of home»</h2>
	  <div>
		<p>Ежедневно нас окучивает нескончаемый поток рекламной информации. Сложно не согласиться с тем, а особенно взяв пример с себя,
		 что наше сознание выработало некий иммунитет и научилось приспосабливаться, ставить непреодолимые барьеры к потокам информационного шума,
		  в частности к классической ТВ рекламе, а ситуация с видео-бордами, баннерами, видео-киосками и их восприятием того плачевней.  </p>
	  </div>
	  <div>
		<p>Не сложно догадаться, что время, а уж тем более деньги, которые вложил рекламодатель потрачены в пустую. Исход плачевный – целевые действия
		 не совершены по причине отсутствия структурированного и релевантного медиа-плана, да и контента в целом в Digital Signage, используемых в ТРЦ.</p>
