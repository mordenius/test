	  <h2>Рішення Smart Target</h2>
	  <div>
		<p>Що ж було розроблено в результаті? Smart target - система, що відповідає за персоналізацію рекламного контента і включає в себе додаток Smart Discount. В даному випадку був використаний дискаунтовий контент ТРК.</p>
	  </div>
	  <div class="clearfix"></div>
	</div>
	<img src="img/news/MediaHackWeekend/KW.MHV.Image_6.jpg" title="" alt="" />
	<div class="clearfix"></div>
	<div class="text-wrapper">
	  <h2>Додаток Smart Discount</h2>
	  <div>
		<p>Що це таке і як з цим працювати? Встановивши додаток Smart Discount, користувач отримує знижки або інформаційний рекламний контент відповідний своїм індивідуальним бажанням. </p>
	  </div>
	  <div>
		<p>Проходячи повз відеоборд або digital кіоск в ТРК, система Smart target показує персоналізовану рекламу в рамках інтересів користувача за допомогою обміну інформацією по Bluetooth між цифровою вивіскою і смартфоном.</p>
	  </div>
	  <div class="clearfix"></div>
