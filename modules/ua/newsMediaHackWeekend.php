	<h2>Future Media Lab</h2>
	  <div>
		<p>З 16 по 18 жовтня 2015 в рамках Future Media Lab відбувся масштабний в Україні хакатон Media Hack Weekend на легендарній кіностудії імені Довженка. Група компаній «1 + 1 Медіа» зібрала найкреативніших молодих людей для того, що б познайомити їх з професіоналами медіаринку, технологічного бізнесу, потенційними інвесторами і дати унікальну можливість запалити фахівців своїми намірами. </p>
	  </div>
	  <div>
		<p>Або ж іншими словами така собі гра з кращими, в результаті якої можна прийти з ідеєю, а піти з першим корпоративним клієнтом.
Протягом цих днів учасники ламали голови над винаходами, які зроблять Інтернет, телебачення, комп'ютерні ігри такими, що будуть вражати ще більше. І звичайно ж команда KVINTO з партнерами LiveBoard, у складі єдиної команди, не стали винятком і взяли участь у наймасштабнішому медіа-заході України.
  </p>
