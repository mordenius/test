	  <h2>Є запит – є реклама</h2>
	  <div>
		<p>Метою став вибірковий підхід до виводу рекламної інформації, на основі переваг кожного користувача цієї системи. Споживач отримує фільтрування непотрібної, нав'язливою і набридливої інформації. Рекламодавець у свою чергу, гарантоване донесення матеріалу виключно цільовій аудиторії.</p>
	  </div>
	  <div>
		<p>Звичайна тв реклама програється по прайм тайму і немає ніякого фідбеку про те, хто подивився цю рекламу і чи була вона йому цікава. Digital target може відстежити цільової потік і перевести оплату за показ реклами не в ефірний час, а за конкретні цільові перегляди. Ну і для власників рекламних майданчиків - підвищення якості послуг.</p>
