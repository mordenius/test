	  <h2>Проблема напрямку digital signage «out of home»</h2>
	  <div>
		<p>Щодня нас охоплює потік рекламної інформації. Складно не погодитися з тим, а особливо взявши приклад з себе, що наша свідомість виробила імунітет і навчилася пристосовуватися, ставити нездоланні бар'єри до потоків інформаційного шуму, зокрема до класичної ТВ реклами, а ситуація з відео-бордами, банерами, відео-кіосками і їх сприйняттям того гірша.  </p>
	  </div>
	  <div>
		<p>Не складно здогадатися, що час, а вже тим більше гроші, які вклав рекламодавець, витрачені дарма. Результат плачевний - цільові дії не виконані через відсутність структурованого і релевантного медіа-плану, та й контенту в цілому в Digital Signage, що використовуються в ТРК.</p>
	  </div>
