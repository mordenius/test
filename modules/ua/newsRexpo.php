	<h2>Конгрес шеф-кухарів FONTEGRO UKRAINE</h2>
	  <div>
		<p>Вперше з 4 по 6 листопада 2015 року, в рамках ресторанної виставки ЕКСПО 2015, Конгрес шеф-кухарів FONTEGRO UKRAINE на спільному стенді з компанією "НОВИЙ ПРОЕКТ" представили кулінарне шоу від кращих шеф-кухарів України. Digital підтримкою в даному проекті виступила компанія KVINTO c партнерами LiveBoard і Samsung, де продемонструвала потенційні можливості візуалізації інформації за допомогою цифрових технологій, представила </p>
	  </div>
	  <div>
		<p>прототип електронного меню і показала широкий функціонал системи Digital Signage з віддаленним управлінням контентом в режимі реального часу.
Кожен день на стенді гості могли не тільки відвідувати майстер-класи шеф-кухарів, дегустувати їх кулінарні шедеври, а й упевнитися, на скільки технології Digital Signage допомагають людині (споживачеві, клієнтові, спікеру і т.д.) в такому сегменті, як HoReCa , а саме:</p>
	  </div>
