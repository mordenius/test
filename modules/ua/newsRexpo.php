	  <h2>Взаємодія та залучення в процес роботи </h2>
	  <div>
		<p>Компанія KVINTO мала можливість продемонструвати прототип електронного меню з певним функціоналом, розробленим спеціально під формат заходу. Цей додаток включав в себе можливості:
</p>
	  </div>
	  <div>
		<p>- замовлення для дегустації будь-якої страви зі списку кухаря;<br>
			- перегляду розкладу й анонсів майстер-класів на найближчі дні;<br>
		  - ознайомитися більш детально з кожним спікером;<br>
			- дати оцінку та проголосувати за вподобані страви;<br>
			- отримання зворотного зв'язку від учасників тієї чи іншої сфери діяльності, представленої на стенді (KVINTO, LiveBoard, Samsung, Новий проект, Форвард);<br>
			- збору даних про користувачів для подальшої комунікації.</p>
