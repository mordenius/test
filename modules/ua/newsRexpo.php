	  <h2>Легкість сприйняття </h2>
	  <div>
		<p>Протягом 3-х днів конгресу в режимі нон-стоп будь-який гість та учасник завжди був проінформований і обізнаний про поточні та майбутні події на стенді. Система показувала анонси, таймінг виступів, промо партнерів/учасників, он-лайн трансляцію роботи шеф-кухарів і демонструвала їх кулінарні шедеври. Що ж у підсумку ми отримали? </p>
	  </div>
	  <div>
		<p>Потік обізнаної, а головне зацікавленої цільової аудиторії в тих чи інших сферах діяльності представлених на стенді. Що з цього може отримати заклад громадського харчування? Все той же проінформмований потік цільової аудиторії, який знає що й коли відбуватиметься у вас в закладі. </p>
