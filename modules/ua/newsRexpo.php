	  <h2>Своєчасність подання інформації</h2>
	  <div>
		<p>Шеф-кухар, створюючи нове кулінарне творіння, розуміє, що життєвий цикл страви закінчується рівно на тому моменті, як тільки столовий прибор його руйнує та в пам'яті залишається виключно смак. В даному випадку спікер мав можливість демонструвати на екранах результат своєї діяльності протягом усього дня, а також, що є немало важливим для самого шефа - </p>
	  </div>
	  <div>
		<p>донести коротку інформацію про себе та своїх переконаннях, філософію приготування, тематику виступу й найменування фірмових страв. Використання таких технологій у сегменті HoReCa створює безумовний wow-ефект. Також взаємодіє з аудиторією, за рахунок актуальної, своєчасної та цільової подачі інформації паралельно з он-лайн трансляцією й описом технологій.</p>
	  </div>
